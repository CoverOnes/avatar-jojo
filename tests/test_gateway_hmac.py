"""§24.1 gateway HMAC verifier tests.

Two layers:

1. Pure-function / dependency tests (no Redis) — valid tuple passes, every
   tamper variant returns 401, dev short-circuit passes. These mount the real
   ``verify_gateway_hmac`` dependency on a throwaway FastAPI route and drive it
   through Starlette's TestClient so the body-reading path is exercised end to
   end.

2. Nonce/replay tests — spin a REAL Redis via testcontainers-python and assert a
   replayed ``X-Request-ID`` is rejected. Skipped (not silently passed) when
   Docker is unavailable.

The expected-signature helper here is an INDEPENDENT reimplementation of the
canonical string (it does NOT call the production ``compute_gateway_signature``)
so "valid → passes" is not a tautology. In addition, ``test_go_cross_check_vector``
pins two signatures generated by the Go signer itself, proving byte-for-byte
agreement across languages.
"""

from __future__ import annotations

import collections.abc as cabc
import hashlib
import hmac
import time

import pytest
from fastapi import Depends, FastAPI
from fastapi.testclient import TestClient

from avatar.api.security import GATEWAY_BODY_LIMIT, verify_gateway_hmac
from avatar.config import get_settings

# 32-char placeholder HMAC secret — test only, NOT a real secret. Matches the Go
# test ``testSecret`` so the cross-check vectors below line up.
TEST_SECRET = "0123456789abcdef0123456789abcdef"

# Fixed identity tuple shared across the table.
FIX_USER_ID = "11111111-1111-1111-1111-111111111111"
FIX_KYC_TIER = "2"
FIX_ACCOUNT_TYPE = "PERSONAL"
FIX_EMAIL_VERIFIED = "true"
FIX_REQUEST_ID = "req-abc"


# --- independent signer (NOT the production function) -------------------------


def _independent_sign(
    secret: str,
    method: str,
    path: str,
    body: bytes,
    *,
    user_id: str = FIX_USER_ID,
    kyc_tier: str = FIX_KYC_TIER,
    account_type: str = FIX_ACCOUNT_TYPE,
    email_verified: str = FIX_EMAIL_VERIFIED,
    request_id: str = FIX_REQUEST_ID,
    ts: str,
) -> str:
    """Independently derive the §24.1 hex signature (mirrors the Go test helper)."""
    body_hash_hex = hashlib.sha256(body).hexdigest()
    identity = "|".join([user_id, kyc_tier, account_type, email_verified, request_id, ts])
    canonical = (
        f"{len(method)}\n{method}\n"
        f"{len(path)}\n{path}\n"
        f"{len(body_hash_hex)}\n{body_hash_hex}\n"
        f"{identity}"
    )
    return hmac.new(secret.encode("utf-8"), canonical.encode("utf-8"), hashlib.sha256).hexdigest()


def _signed_headers(
    secret: str,
    method: str,
    path: str,
    body: bytes,
    ts: int,
    *,
    account_type: str = FIX_ACCOUNT_TYPE,
    request_id: str = FIX_REQUEST_ID,
) -> dict[str, str]:
    ts_str = str(ts)
    sig = _independent_sign(
        secret,
        method,
        path,
        body,
        account_type=account_type,
        request_id=request_id,
        ts=ts_str,
    )
    return {
        "X-User-Id": FIX_USER_ID,
        "X-Kyc-Tier": FIX_KYC_TIER,
        "X-Account-Type": account_type,
        "X-Email-Verified": FIX_EMAIL_VERIFIED,
        "X-Request-ID": request_id,
        "X-Gateway-Ts": ts_str,
        "X-Gateway-Signature": sig,
    }


# --- test app + settings override --------------------------------------------


def _build_client(
    monkeypatch: pytest.MonkeyPatch,
    *,
    secret: str,
    nonce_redis_url: str = "",
) -> TestClient:
    """Build a tiny app whose protected routes require verify_gateway_hmac.

    Overrides the gateway settings (conftest forces an empty secret autouse) and
    clears the settings cache so the override takes effect.
    """
    monkeypatch.setenv("GATEWAY_HMAC_SECRET", secret)
    monkeypatch.setenv("GATEWAY_NONCE_REDIS_URL", nonce_redis_url)
    get_settings.cache_clear()

    app = FastAPI()

    @app.get("/protected", dependencies=[Depends(verify_gateway_hmac)])
    async def _protected_get() -> dict[str, bool]:
        return {"ok": True}

    @app.post("/protected", dependencies=[Depends(verify_gateway_hmac)])
    async def _protected_post() -> dict[str, bool]:
        return {"ok": True}

    return TestClient(app)


