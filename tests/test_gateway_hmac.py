def test_cross_endpoint_replay_rejected_401(monkeypatch: pytest.MonkeyPatch) -> None:
    # A signature over POST /protected with one body must not verify on a
    # different path. Here we sign over a body+path and resend the signature on a
    # request whose body differs → path/body binding rejects it.
    now = int(time.time())
    client = _build_client(monkeypatch, secret=TEST_SECRET)
    body = b'{"amount":100}'
    headers = _signed_headers(TEST_SECRET, "POST", "/protected", body, now)
    # Reuse the signature with a different body → mismatch.
    resp = client.post("/protected", headers=headers, content=b'{"amount":999}')
    assert resp.status_code == 401, "replay with altered body must be 401"


def test_oversized_body_rejected_413(monkeypatch: pytest.MonkeyPatch) -> None:
    # A body just past GATEWAY_BODY_LIMIT must be rejected with 413 (not the
    # generic 401) and never fully buffered — mirrors the Go verifier's
    # BODY_TOO_LARGE / io.LimitReader(limit+1) path. The signature is computed
    # over the oversized body so the rejection is the size cap, not a sig
    # mismatch (the size check fires before signature verification).
    now = int(time.time())
    client = _build_client(monkeypatch, secret=TEST_SECRET)
    body = b"x" * (GATEWAY_BODY_LIMIT + 1)
    headers = _signed_headers(TEST_SECRET, "POST", "/protected", body, now)
    resp = client.post("/protected", headers=headers, content=body)
    assert resp.status_code == 413, "oversized body must be 413 BODY_TOO_LARGE"


# --- byte-for-byte cross-check against the Go signer -------------------------


def test_go_cross_check_vector(monkeypatch: pytest.MonkeyPatch) -> None:
    """Pin signatures the GO signer produced; assert the Python verifier accepts.

    These hex digests were generated by a standalone Go program using the SAME
    crypto/hmac + crypto/sha256 + fmt.Sprintf canonical layout as
    gateway/internal/platform/middleware/identity.go and
    user/internal/platform/middleware/gateway_signature.go. If the Python
    canonical string drifts by a single byte these fail.

    Canonical string (documented for a Go-side cross-check test):
        {len(method)}\\n{method}\\n{len(path)}\\n{path}\\n
        {len(bodyHashHex)}\\n{bodyHashHex}\\n
        {X-User-Id}|{X-Kyc-Tier}|{X-Account-Type}|{X-Email-Verified}|{rid}|{ts}

    Vector 1 (POST, JSON body):
        secret = "0123456789abcdef0123456789abcdef"
        method = "POST"   path = "/api/avatar/sessions"
        body   = {"room":"demo","identity":"u1"}
        id tuple = 11111111-1111-1111-1111-111111111111 | 2 | PERSONAL | true
                   | req-abc | 1750000000
        → 5cda5e00991d0a2ce0e849fcbbb1faf0e4a4a18ee15c82bad4c29963ef77a6ab

    Vector 2 (GET, empty body):
        method = "GET"    path = "/api/avatar/health"   body = b""
        id tuple = 11111111-... | 0 | PERSONAL | false | req-xyz | 1750000000
        → a5eeeff2a20d4b9b81bfea9a935f0c4eeae18b82a988d3e2fea46459e470ba33
    """
    monkeypatch.setenv("GATEWAY_HMAC_SECRET", TEST_SECRET)
    get_settings.cache_clear()

    from avatar.api.security import compute_gateway_signature

    # Vector 1 — POST with body.
    sig1 = compute_gateway_signature(
        TEST_SECRET.encode("utf-8"),
        method="POST",
        path="/api/avatar/sessions",
        body=b'{"room":"demo","identity":"u1"}',
        user_id="11111111-1111-1111-1111-111111111111",
        kyc_tier="2",
        account_type="PERSONAL",
        email_verified="true",
        request_id="req-abc",
        ts="1750000000",
    ).hex()
    assert sig1 == "5cda5e00991d0a2ce0e849fcbbb1faf0e4a4a18ee15c82bad4c29963ef77a6ab", (
        "Python signature must match the Go signer byte-for-byte (vector 1)"
    )

    # Vector 2 — GET, empty body, different tier/email/rid.
    sig2 = compute_gateway_signature(
        TEST_SECRET.encode("utf-8"),
        method="GET",
        path="/api/avatar/health",
        body=b"",
        user_id="11111111-1111-1111-1111-111111111111",
        kyc_tier="0",
        account_type="PERSONAL",
        email_verified="false",
        request_id="req-xyz",
        ts="1750000000",
    ).hex()
    assert sig2 == "a5eeeff2a20d4b9b81bfea9a935f0c4eeae18b82a988d3e2fea46459e470ba33", (
        "Python signature must match the Go signer byte-for-byte (vector 2)"
    )


# --- nonce / replay (real Redis via testcontainers) --------------------------


@pytest.fixture
def redis_url() -> cabc.Iterator[str]:
    """Spin a real Redis container; skip (not pass) when Docker is unavailable."""
    docker = pytest.importorskip(
        "docker", reason="docker SDK not installed — skipping Redis replay tests"
    )
    try:
        from testcontainers.redis import RedisContainer
    except ImportError:  # pragma: no cover
        pytest.skip("testcontainers[redis] not installed")

    try:
        docker.from_env().ping()
    except Exception as exc:  # noqa: BLE001
        pytest.skip(f"Docker daemon unavailable: {exc}")

    with RedisContainer("redis:7-alpine") as container:
        host = container.get_container_host_ip()
        port = container.get_exposed_port(6379)
        yield f"redis://{host}:{port}/0"


def test_replay_same_request_id_rejected(monkeypatch: pytest.MonkeyPatch, redis_url: str) -> None:
    now = int(time.time())
    client = _build_client(monkeypatch, secret=TEST_SECRET, nonce_redis_url=redis_url)

    # Same request_id reused → first passes (fresh nonce), second is replay → 401.
    headers = _signed_headers(TEST_SECRET, "GET", "/protected", b"", now, request_id="replay-rid-1")
    first = client.get("/protected", headers=headers)
    assert first.status_code == 200, "first signed request with fresh nonce must pass"

    second = client.get("/protected", headers=headers)
    assert second.status_code == 401, "second request with same rid must be 401 (replay)"


